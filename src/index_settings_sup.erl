-module(index_settings_sup).

-export([start_link/0]).
-export([init/1]).

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

init([]) ->
    Children =
        [{index_settings_events,
          {index_settings_manager, start_link_event_manager, []},
          permanent, brutal_kill, worker, [index_settings_manager]},
         {index_settings_manager, {index_settings_manager, start_link, []},
          permanent, 1000, worker, [index_settings_manager]}],
    {ok, {{one_for_all,
           misc:get_env_default(max_r, 3),
           misc:get_env_default(max_t, 10)},
          Children}}.
